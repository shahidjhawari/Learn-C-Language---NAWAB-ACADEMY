#include <stdio.h>

int main() {
    printf("Hello \n World"); // new line par jane keliye
    printf("Hello \t World"); // Tab space add karne keliye
    printf("Hello \" World"); // Double qoute add karne keliye
    printf("Hello \' World"); // Single qoute add karne keliye
    printf("Hello \\ World"); // Back Slash lagane keliye
    printf("Hello \a World"); // Beep ki awaz nikalne keilye
    printf("Hello \r World"); // Line break kar ke shuru me jane keliye
    printf("Hello \f World"); // Use nai hota
    printf("Hello \v World"); // Use nai hota
}

